lateinit var binding: FragmentQuranBinding
    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        binding=FragmentQuranBinding.inflate(
            LayoutInflater.from(context),container,false)
        return binding.root
    }



    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)
        initViews(view)
    }

    private val chapterAdapter = ItemChapterAdapter(cahpterTitle)

    private fun initViews(view: View) {
        chapterAdapter.onItemClickListener =
            ItemChapterAdapter.OnItemClickListener { position, title ->
                startChapterDeteails(position, title)
            }

        binding.chapterRv.adapter = chapterAdapter
    }

    private fun startChapterDeteails(
        position: Int, title: String
    ) {
        val intent = Intent(activity, ChapterDetailsActivity::class.java)
        intent.putExtra(ChapterDetailsActivity.EXTRA_POSITION, position)
        intent.putExtra( ChapterDetailsActivity.EXTRA_TITLE, title)
        startActivity(intent)
    }
